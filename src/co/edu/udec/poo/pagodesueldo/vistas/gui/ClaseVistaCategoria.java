        ClaseCategoria oCategoria = recuperarDatosGUI();
        
        String strSentenciaUpdate = String.format("UPDATE TABCATEGORIA SET CODIGO_CATE='%s',NOMBRE='%s',SUELDO='%s' WHERE CODIGO_CATE = '%d'",oCategoria.getCODIGO(), oCategoria.getNOMBRE(), oCategoria.getSUELDO(),oCategoria.getCODIGO());
        
        objClaseEmpleadoCRUD.ejecutarSentenciaQLS(strSentenciaUpdate);
        
        
        mostrarDatos();
        clening ();
        

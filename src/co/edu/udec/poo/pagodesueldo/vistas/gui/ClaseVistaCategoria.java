        public ClaseCategoria recuperarDatosGUI(){
        ClaseCategoria oCategoria = new ClaseCategoria();
        
         oCategoria.setCODIGO(Integer.parseInt(txtCodigo.getText()));
         oCategoria.setNOMBRE((txtNombre.getText()));
         oCategoria.setSUELDO(txtSueldo.getText());

         return oCategoria;
        
    
    }
        
        
        public void mostrarDatos(){
        
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        
        ClaseCategoriaCRUD objClaseCategoriaCRUD = new ClaseCategoriaCRUD(); //CONEXION BD
        
        try {
            ResultSet resultado = objClaseCategoriaCRUD.consultarRegistros("SELECT * FROM TABCATEGORIA");
            
            while (resultado.next()) {
                //System.out.println(resultado.getString("ID"));
                System.out.println(resultado.getString("CODIGO_CATE"));
                System.out.println(resultado.getString("NOMBRE"));
                System.out.println(resultado.getString("SUELDO"));
                
                Object[] oUsuario={resultado.getString("CODIGO_CATE"),resultado.getString("NOMBRE"),resultado.getString("SUELDO")};
                
                modelo.addRow(oUsuario);
                
            }

        } catch (Exception e) {
            System.out.println(e);
        }
        
    }
    
    


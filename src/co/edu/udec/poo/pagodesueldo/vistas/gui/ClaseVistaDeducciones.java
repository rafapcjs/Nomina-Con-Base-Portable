        public void mostrardatos() {
        while (modelDeducciones.getRowCount() > 0) {
            modelDeducciones.removeRow(0);
        }
        ClaseDeduccionesCRUD conexión = new ClaseDeduccionesCRUD();
        try {
            ResultSet resultado = conexión.consultardato("SELECT*FROM TabDeducciones");
            while (resultado.next()) {
                System.out.println(resultado.getString("iddeducciones"));

                System.out.println(resultado.getString("Nombred"));
                System.out.println(resultado.getString("Porcentaje"));
                System.out.println(resultado.getString("RangoInicial"));
                System.out.println(resultado.getString("RangoFinal"));
                Object[] oUsuario = {resultado.getString("iddeducciones"), resultado.getString("Nombred"), resultado.getString("Porcentaje"), resultado.getString("RangoInicial"), resultado.getString("RangoFinal")};
                modelDeducciones.addRow(oUsuario);
            }
        } catch (Exception e) {
            System.out.println(e);

        }
    }
    
    
    
    

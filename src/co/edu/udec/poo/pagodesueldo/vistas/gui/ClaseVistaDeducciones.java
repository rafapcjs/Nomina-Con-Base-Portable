    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed

        int k = JOptionPane.showConfirmDialog(null, "desea eliminar  ");
        if (k==0) {                  JOptionPane.showMessageDialog(null, " se elimino correctamente");
        
        
        ClaseDeduccionesCRUD conexión = new ClaseDeduccionesCRUD();
        ClaseDeducciones clasededucciones = recuperardatos();
        String strSentenciaInsert = String.format("DELETE FROM  TabDeducciones WHERE iddeducciones=%d", clasededucciones.getIdDeduccion());
        conexión.ejecuarsentencia(strSentenciaInsert);

        this.mostrardatos();
        }
        else   {
            JOptionPane.showMessageDialog(null, " No elimino correctamente");
        }
              
    }//GEN-LAST:event_EliminarActionPerformed


    public ClaseDeducciones recuperardatos() {
        ClaseDeducciones clasededucciones = new ClaseDeducciones();
        int Iddeducciones = Textid.getText().isEmpty() ? 0 : Integer.parseInt(Textid.getText());
        clasededucciones.setIdDeduccion(Iddeducciones);
        clasededucciones.setNombre(textnombre.getText());
        clasededucciones.setPorcentajeDedu(Integer.parseInt(textporcentaje.getText()));
        clasededucciones.setValorRangoInicial(Integer.parseInt(Textrangoinicial.getText()));

        clasededucciones.setValorRangoFinal(Integer.parseInt(textrangofinal.getText()));
        return clasededucciones;

    }
    
    
    
    
